#Given a string that contains only digits 0-9 and a target value, return all possibilities to add binary operators (not unary) +, -, or * between the digits so they evaluate to the target value.
class Solution(object):
    def addOperators(self, num, target):
        """
        :type num: str
        :type target: int
        :rtype: List[str]
        """
        out = []
        #total: the total value of calculations until this time
        #lastOP: the last operation among + or -
        def compute(total,lastVal,n,path,lastOp):
            if(len(n)==0):
                if(total==target):
                    out.append(path)
            for i in range(1,len(n)+1): #generate next number with differnet length
                if (len(n[:i])!= 1 and n[:i][0] =='0'):
                    continue
                val=int(n[:i])
                compute(total+val,val,n[i:],path+"+"+n[:i],"+")
                compute(total-val,val,n[i:],path+"-"+n[:i],"-")
                if("+" in path or "-" in path): #in order to do *, we need to know the last + or - operation
                    if(lastOp=="+"):
                        compute(total-lastVal+lastVal*val,lastVal*val,n[i:],path+"*"+n[:i],"+")
                    elif(lastOp=="-"):
                        compute(total+lastVal-lastVal*val,lastVal*val,n[i:],path+"*"+n[:i],"-")
                else:
                    compute(total * val, val, n[i:], path + "*" + n[:i],"*")

        for i in range(1,len(num)+1):# generate first number with different length
            if(len(num[:i])==1 or num[:i][0]!='0'):# because 05 is not acceptable
                compute(int(num[:i]),int(num[:i]),num[i:],num[:i],"-")
        return out






